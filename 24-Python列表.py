# 1. 列表的定义：
'''
基本语法：
列表序列名称 = [元素1，元素2，元素3，...]
''' 
# 注：列表可以一次存储多个数据且可以为不同的数据类型
    
# 案例：定义一个列表，用于保存苹果，香蕉，菠萝
list1 = ['apple','banana','pineapple']
# 列表类型支持直接打印
print(list1)
# 打印列表的数据类型
print(type(list1))

